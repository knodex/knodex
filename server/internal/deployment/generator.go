// GenerateDeleteCommitMessage creates a commit message for deleting a manifest
func (g *Generator) GenerateDeleteCommitMessage(namespace, name, rgdName, deletedBy string) string {
	var sb strings.Builder

	sb.WriteString(fmt.Sprintf("Delete %s/%s (RGD: %s)\n", namespace, name, rgdName))
	sb.WriteString("\n")
	sb.WriteString(fmt.Sprintf("Deleted by: %s\n", deletedBy))
	sb.WriteString(fmt.Sprintf("Timestamp: %s\n", time.Now().Format(time.RFC3339)))
	sb.WriteString("\n")
	sb.WriteString("Managed by Knodex GitOps")

	return sb.String()
}

// GenerateKustomization creates a kustomization.yaml file for a namespace
func (g *Generator) GenerateKustomization(namespace string, resources []string) (string, error) {
	if namespace == "" {
		return "", fmt.Errorf("namespace is required")
	}

	kustomization := map[string]interface{}{
		"apiVersion": "kustomize.config.k8s.io/v1beta1",
		"kind":       "Kustomization",
		"namespace":  namespace,
		"resources":  resources,
	}

	yamlStr, err := parser.ToYAMLString(kustomization)
	if err != nil {
		return "", fmt.Errorf("failed to marshal kustomization to YAML: %w", err)
	}

	header := "# Kustomization for Knodex managed resources\n# DO NOT EDIT - automatically generated\n---\n"
	return header + yamlStr, nil
}
