// --- Security Validation Functions ---

// ValidateSubjectIdentifier validates identifiers for users, groups, and roles
// to prevent injection attacks via subject manipulation
func ValidateSubjectIdentifier(id string) error {
	if id == "" {
		return fmt.Errorf("identifier cannot be empty")
	}

	// Check length limit
	if len(id) > MaxSubjectIDLength {
		return fmt.Errorf("identifier exceeds maximum length of %d", MaxSubjectIDLength)
	}

	// Disallow control characters, colons (prefix separator), and spaces
	if strings.ContainsAny(id, ":\n\r\t ") {
		return fmt.Errorf("identifier contains invalid characters (no colons, newlines, or spaces allowed)")
	}

	// Ensure it doesn't start with reserved prefixes (case-insensitive)
	lower := strings.ToLower(id)
	forbidden := []string{"role:", "user:", "group:", "proj:"}
	for _, prefix := range forbidden {
		if strings.HasPrefix(lower, prefix) {
			return fmt.Errorf("identifier cannot start with reserved prefix: %s", prefix)
		}
	}

	return nil
}

// ValidateGlobPattern validates glob patterns to prevent ReDoS attacks
func ValidateGlobPattern(pattern string) error {
	if pattern == "" {
		return nil // Empty patterns are allowed
	}

	// Check pattern length
	if len(pattern) > MaxPatternLength {
		return fmt.Errorf("pattern too long (max %d chars): %d", MaxPatternLength, len(pattern))
	}

	// Count wildcards
	wildcardCount := strings.Count(pattern, "*") + strings.Count(pattern, "?")
	if wildcardCount > MaxWildcards {
		return fmt.Errorf("pattern has too many wildcards (max %d): %d", MaxWildcards, wildcardCount)
	}

	// Disallow nested or consecutive wildcards like ** or ??
	if strings.Contains(pattern, "**") || strings.Contains(pattern, "??") {
		return fmt.Errorf("nested or consecutive wildcards not allowed")
	}

	return nil
}

// ValidateEffect validates the policy effect
func ValidateEffect(eft string) error {
	if eft != EffectAllow && eft != EffectDeny {
		return fmt.Errorf("invalid effect: must be '%s' or '%s', got '%s'", EffectAllow, EffectDeny, eft)
	}
	return nil
}

// --- Secure Formatting Functions ---

// FormatObject creates a properly formatted object string for policy matching
// Returns an error if the resource name contains invalid characters
func FormatObject(resourceType, resourceName string) (string, error) {
	if resourceName == "" || resourceName == "*" {
		return resourceType + "/*", nil
	}

	// Validate resource name length
	if len(resourceName) > MaxResourceNameLength {
		return "", fmt.Errorf("resource name exceeds maximum length of %d", MaxResourceNameLength)
	}

	// Sanitize resource name to prevent glob injection
	sanitized := sanitize.GlobCharacters(resourceName)
	return resourceType + "/" + sanitized, nil
}

// FormatObjectUnsafe creates an object string without sanitization
// WARNING: Only use this for trusted/validated input (e.g., built-in policies)
func FormatObjectUnsafe(resourceType, resourceName string) string {
	if resourceName == "" || resourceName == "*" {
		return resourceType + "/*"
	}
	return resourceType + "/" + resourceName
}

// FormatProjectRole creates a project-specific role name
// Returns an error if project or role name contains invalid characters
func FormatProjectRole(projectName, roleName string) (string, error) {
	if len(projectName) > MaxProjectNameLength {
		return "", fmt.Errorf("project name exceeds maximum length of %d", MaxProjectNameLength)
	}
	if len(roleName) > MaxRoleNameLength {
		return "", fmt.Errorf("role name exceeds maximum length of %d", MaxRoleNameLength)
	}

	if err := ValidateSubjectIdentifier(projectName); err != nil {
		return "", fmt.Errorf("invalid project name: %w", err)
	}
	if err := ValidateSubjectIdentifier(roleName); err != nil {
		return "", fmt.Errorf("invalid role name: %w", err)
	}

	return fmt.Sprintf("proj:%s:%s", projectName, roleName), nil
}

// FormatUserSubject creates a user subject string
// Returns an error if userID contains invalid characters
func FormatUserSubject(userID string) (string, error) {
	if err := ValidateSubjectIdentifier(userID); err != nil {
		return "", fmt.Errorf("invalid user ID: %w", err)
	}
	return "user:" + userID, nil
}

