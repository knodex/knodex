// ClearPolicies removes all policies (except built-in)
func (ce *CasbinEnforcer) ClearPolicies() error {
	ce.mu.Lock()
	defer ce.mu.Unlock()

	// Clear all policies
	ce.enforcer.ClearPolicy()

	// Reload built-in policies while holding the lock (fixes race condition)
	return loadBuiltinPoliciesLocked(ce.enforcer)
}

