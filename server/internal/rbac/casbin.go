//go:embed casbin_model.conf
var casbinModelFS embed.FS

// CasbinEnforcer wraps Casbin enforcer with helper methods for RBAC operations
type CasbinEnforcer struct {
	enforcer *casbin.Enforcer
	mu       sync.RWMutex
}

// NewCasbinEnforcer creates a new Casbin enforcer with built-in policies
func NewCasbinEnforcer() (*CasbinEnforcer, error) {
	// Load model from embedded file
	modelContent, err := casbinModelFS.ReadFile("casbin_model.conf")
	if err != nil {
		return nil, fmt.Errorf("failed to read casbin model: %w", err)
	}

	// Parse model
	m, err := model.NewModelFromString(string(modelContent))
	if err != nil {
		return nil, fmt.Errorf("failed to parse casbin model: %w", err)
	}

	// Create enforcer with in-memory adapter (no adapter = in-memory)
	enforcer, err := casbin.NewEnforcer(m)
	if err != nil {
		return nil, fmt.Errorf("failed to create casbin enforcer: %w", err)
	}

	// Enable auto-save (policies persist to adapter automatically)
	enforcer.EnableAutoSave(false)

	// Load built-in policies (no lock needed during initialization - single-threaded)
	if err := loadBuiltinPoliciesLocked(enforcer); err != nil {
		return nil, fmt.Errorf("failed to load built-in policies: %w", err)
	}

	return &CasbinEnforcer{enforcer: enforcer}, nil
}

// NewCasbinEnforcerFromString creates an enforcer from a model string (for testing)
func NewCasbinEnforcerFromString(modelText string) (*CasbinEnforcer, error) {
	m, err := model.NewModelFromString(modelText)
	if err != nil {
		return nil, fmt.Errorf("failed to parse casbin model: %w", err)
	}

	enforcer, err := casbin.NewEnforcer(m)
	if err != nil {
		return nil, fmt.Errorf("failed to create casbin enforcer: %w", err)
	}

	enforcer.EnableAutoSave(false)

	// Load built-in policies (no lock needed during initialization - single-threaded)
	if err := loadBuiltinPoliciesLocked(enforcer); err != nil {
		return nil, fmt.Errorf("failed to load built-in policies: %w", err)
	}

	return &CasbinEnforcer{enforcer: enforcer}, nil
}

// loadBuiltinPoliciesLocked adds global roles and default policies
// IMPORTANT: Caller MUST hold the mutex lock before calling this method
//
// This function is the SINGLE SOURCE OF TRUTH for all built-in role permissions.
// All role-based permissions are consolidated here. See docs/rbac-permissions.md for the
// complete permission reference.
//
// Server-Level Role Model:
// - role:serveradmin - Full access to everything including server-level operations (SSO, audit, license)
//
// Project-scoped roles (admin, developer, readonly) are defined in Project CRD spec.roles
// and auto-generated by getBuiltInProjectPolicies(). No global readonly role exists.
//
// Custom roles (platform-admin, developer, etc.) should be defined in Project CRD with explicit policies.
func loadBuiltinPoliciesLocked(e *casbin.Enforcer) error {
	// Built-in policies for server-level role model
	// Format: {subject, object, action, effect}
	policies := [][]string{
		// ========================================
		// SERVERADMIN - Full access to everything including server-level operations
		// ========================================
		// Wildcard policy for admin bypass checks (e.g., CanAccess(userID, "*", "*"))
		// This enables handlers and middleware to detect admin status via permission checks
		{"role:serveradmin", "*", "*", "allow"},
		// Explicit resource policies (for clarity and documentation)
		{"role:serveradmin", "projects/*", "*", "allow"},
		{"role:serveradmin", "rgds/*", "*", "allow"},
		{"role:serveradmin", "instances/*", "*", "allow"},
		{"role:serveradmin", "users/*", "*", "allow"},
		{"role:serveradmin", "applications/*", "*", "allow"},
		{"role:serveradmin", "repositories/*", "*", "allow"},
		{"role:serveradmin", "namespaces/*", "*", "allow"},
		// Settings access for serveradmin (SSO, audit, license)
		{"role:serveradmin", "settings/*", "*", "allow"},
		// Compliance access for serveradmin (Enterprise: OPA Gatekeeper compliance view)
		{"role:serveradmin", "compliance/*", "*", "allow"},
	}

	// Add policies
	for _, policy := range policies {
		if _, err := e.AddPolicy(policy); err != nil {
			return fmt.Errorf("failed to add policy %v: %w", policy, err)
		}
	}

	return nil
}

// Enforce checks if a subject can perform an action on an object
func (ce *CasbinEnforcer) Enforce(sub, obj, act string) (bool, error) {
	ce.mu.RLock()
	defer ce.mu.RUnlock()
	return ce.enforcer.Enforce(sub, obj, act)
}

