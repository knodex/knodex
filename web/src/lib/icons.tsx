/**
 * Get a Lucide icon component by name.
 * @param name - The icon name in kebab-case (e.g., "layout-grid") or PascalCase (e.g., "LayoutGrid")
 * @returns The icon component or LayoutGrid as fallback
 */
export function getLucideIcon(name?: string): React.ComponentType<LucideProps> {
  if (!name) {
    return LayoutGrid;
  }

  // Convert kebab-case to PascalCase for lookup
  const pascalCaseName = name
    .split("-")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join("");

  return ICON_MAP[pascalCaseName] ?? LayoutGrid;
}
